	/**
	 * search
	 */
	public static String[] searchData(NewLink link,String[][] table){
		if(link == null){
			return null;
		}
		for(String[] every :table){
			if ((link.getFrom().equals(every[0]) && link.getEnd().equals(every[1]))
					|| (link.getFrom().equals(every[1]) && link.getEnd().equals(every[0]))) {
					return every;
			}
		}
		return null;
	}
	
